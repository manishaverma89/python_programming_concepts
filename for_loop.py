# for loop

fruits = ["Apple","Orange","Guava","Grapes","Banana"]
for fruit in fruits:
    print(fruit)
    print(fruit + " pie")  #will be printed after every fruit name
print("Have these fruits daily") # This line will be printed once after all fruit names
    
    
